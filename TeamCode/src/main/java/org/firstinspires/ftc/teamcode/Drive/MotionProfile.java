package org.firstinspires.ftc.teamcode.Drive;

public class MotionProfile {
    public final float v0;
    public final float vMax;
    public final float accel;

    public MotionProfile(float v0, float vMax, float accel){
        this.v0 = v0;
        this.vMax = vMax;
        this.accel = accel;
    }
}
